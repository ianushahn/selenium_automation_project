package star_patter;

public class Pattern_C_twentytwo_60 {

	public static void main(String[] args)
	{
		
		for(int i=0;i<5;i++)
		{
			
			for(int j=0;j<=5;j++)
			{
				if(j<=i)
				{
					System.out.print((i%2)+" ");
				
				}
				else
				{
					System.out.print(" ");
				}
				
			}
			System.out.println();
			
			
			
			
		}

	}

}
